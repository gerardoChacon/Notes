// Inicialización de la variable 'i' con el valor 0.
let i = 0;

// Bucle do-while que se ejecuta al menos una vez antes de verificar la condición.
do {
    // Imprime 'una vez' en cada iteración del bucle.
    console.log('una vez');
    
    // Incrementa 'i' en 1 en cada iteración.
    i++;
} while (i < 4); // La condición del bucle: se ejecuta mientras 'i' sea menor que 4.

