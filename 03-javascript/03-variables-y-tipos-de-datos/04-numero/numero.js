// Declaración de una variable 'numero' con un valor entero.
let numero = 5;

// Declaración de una variable 'decimales' con un valor decimal. //JavaScript imprime solo 15 deccimales
let decimales = 5.234265435345938734587987345983745;

// Declaración de una variable 'negativos' con un valor entero negativo.
let negativos = -10;

// Declaración de una variable 'infinito' que representa el infinito matemático.
let infinito = Infinity;

// Declaración de una variable 'NoEsNumero' que representa un valor 'No es un Número' (NaN).
let NoEsNumero = NaN;

// Imprime el valor de la variable 'numero'.
console.log(numero);

// Imprime el valor de la variable 'decimales'.
console.log(decimales);

// Imprime el valor de la variable 'negativos'.
console.log(negativos);

// Imprime el valor de la variable 'infinito'.
console.log(infinito);

// Imprime el negativo del valor 'infinito'.
console.log(-infinito);

// Imprime el valor de la variable 'NoEsNumero'.
console.log(NoEsNumero);

// Imprime el valor de PI de la librería Math.
console.log(Math.PI);

// Redondea el valor de la variable 'decimales' al número entero más cercano.
console.log(Math.round(decimales));

// Genera y muestra un número aleatorio entre 0 y 1.
console.log(Math.random());


