// Declaración de una variable 'texto' con el string "Pink Floyd".
let texto = "Pink Floyd";

// Imprime la longitud del string almacenado en 'texto'.
console.log(texto.length); // Imprime la cantidad de caracteres en "Pink Floyd", que es 10.

// Imprime el string 'texto' convertido a mayúsculas.
console.log(texto.toUpperCase()); // Convierte "Pink Floyd" a "PINK FLOYD" y lo imprime.

// Imprime el string 'texto' convertido a minúsculas.
console.log(texto.toLowerCase()); // Convierte "Pink Floyd" a "pink floyd" y lo imprime.

// Imprime un valor booleano indicando si el string 'texto' incluye la subcadena "Floyd".
console.log(texto.includes('Floyd')); // Verifica si "Pink Floyd" contiene "Floyd". Imprime true.

// Imprime un valor booleano indicando si el string 'texto' incluye la subcadena "Bowie".
console.log(texto.includes('Bowie')); // Verifica si "Pink Floyd" contiene "Bowie". Imprime false.

// Imprime el resultado de dividir el string 'texto' en un arreglo utilizando un espacio como separador.
console.log(texto.split(' ')); // Divide "Pink Floyd" en ["Pink", "Floyd"] y lo imprime.


